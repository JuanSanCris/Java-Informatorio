package ParteIProgramacionImperativa.tiposDeDatos;
// package Clase2ProgramacionImperativa;

public class TiposDeDatos { // Bloque de codigo de clase
    public static void main(String[] args) { // Bloque de codigo de método

        int number1; //Declaración de la variable, del tipo entero o integer

        number1 = 10; //Asignación o inicialización de la variable, con el operador '='

        int number2 = 20; // Declaración y asignación en una misma línea.

        System.out.println(number1 + number2);

        // Para definir una constante, usamos la keyword final y utilizamos la nomencaltura SCREAMING_SNAKE_CASE

        final int vidas = 0;
        System.out.println(vidas);

    }
}
