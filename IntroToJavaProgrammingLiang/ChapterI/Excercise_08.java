package IntroToJavaProgrammingLiang.ChapterI;

/* 
(Area and perimeter of a circle) Write a program that displays the area and perimeter
of a circle that has a radius of 5.5 using the following formula:
perimeter = 2 * radius * pi
area = radius * radius * pi 
*/

public class Excercise_08 {
    public static void main(String[] args) {
        System.out.println("perimeter = " + (2.0 * 5.5 * 3.1415));
        System.out.println("area = " + (5.5 * 5.5 * 3.1415));
    }
}
